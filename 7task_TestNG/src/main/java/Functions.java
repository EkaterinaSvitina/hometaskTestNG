public class Functions {

    public double addition(double a, double b)
    {
        double result = a + b;
        return result;
    }

    public double substraction(double a,double b)
    {
        double result = a - b;
        return result;
    }

    public double multiplication(double a,double b)
    {
        double result = a * b;
        return result;
    }

    public double division(double a,double b)
    {
        double result = a / b;
        return result;
    }

}
